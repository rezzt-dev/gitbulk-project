# ── Logic ────────────────────────────────────────────────────────────────────

_current_lang = "es_ES"

def set_language(lang):
    global _current_lang
    if lang in LANGUAGES:
        _current_lang = lang

def get_current_language():
    return _current_lang

def TR(key, **kwargs):
    text = LANGUAGES.get(_current_lang, LANGUAGES["en_US"]).get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text
